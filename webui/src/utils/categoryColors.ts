export function normalizeCategoryColor(value: string) {
  const key = value.trim()
  if (!key) return 'olCategoryColorNone'
  return outlookColorMap[key.toLowerCase()] ?? key
}

export function categoryColorValue(value: string) {
  const color = normalizeCategoryColor(value)
  return outlookColorValues[color] ?? 0
}

export function categoryOptionColor(value?: string) {
  const colorValue = normalizeCategoryColor(value ?? '')
  return categoryColorOptions.find((option) => option.value === colorValue)?.color ?? categoryColorOptions[0].color
}

export function categoryColorStyle(value?: string) {
  return { backgroundColor: categoryOptionColor(value) }
}

export function categoryTextColor(backgroundColor: string) {
  const hex = backgroundColor.replace('#', '')
  if (hex.length !== 6) return '#172033'
  const red = Number.parseInt(hex.slice(0, 2), 16)
  const green = Number.parseInt(hex.slice(2, 4), 16)
  const blue = Number.parseInt(hex.slice(4, 6), 16)
  const luminance = (red * 299 + green * 587 + blue * 114) / 1000
  return luminance > 150 ? '#172033' : '#ffffff'
}
